import mongoose from "mongoose";

const CampaignSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        required: true
    },
    location: {
        type: String,
        required: true
    },
});

const Campaign = mongoose.model('Campaign', CampaignSchema);

export default Campaign;